package dharm;

public class ball1 {
	private double x;
	private double y;
	private double z;
	public ball1(double x1,double y1,double z1){
		x=x1;y=y1;z=z1;
	}
	public double getx(){
		return x;
	}
	public double gety(){
		return y;
	}
	public double getz(){
		return z;
	}
	public void setxyz(double x,double y,double z){
		this.x=x;this.y=y;this.z=z;
	}
	

}
